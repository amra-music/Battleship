package sample;

public class Delta {
    double x;
    double y;
    double firstLayoutX;
    double firstLayoutY;

    public Delta() {
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getFirstLayoutX() {
        return firstLayoutX;
    }

    public double getFirstLayoutY() {
        return firstLayoutY;
    }
}
