package sample;

public class Delta {
    double x;
    double y;
    double layoutX;
    double layoutY;

    public Delta() {
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getLayoutX() {
        return layoutX;
    }

    public double getLayoutY() {
        return layoutY;
    }
}
